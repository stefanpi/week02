using Systems;

namespace ConsoleApplication
{
    class Program
    {
        static void Main(string[] args)
        {
            int[] = arr = new[]  (10, 20, 15, 70);
            int suma = 0;

            for (int i = 0; i <= arr.Length -1; i++);
            {
                suma = suma + arr [i];

            }

            double media = suma / arr.Length;

            Console.WriteLine(media);
            Console.ReadLine();

        }
    }
}